colors = ["보라", "파랑", "초록", "노랑", "사랑", "노랑"]

idx1 = colors.index("보라")
print(idx1)  # 0 "보라"의 인덱스 위치를 나타냄

idx2 = colors.index("노랑")
print(idx2)    # "노랑"이 가장 처음 위치하는 인덱스를 알려줘

